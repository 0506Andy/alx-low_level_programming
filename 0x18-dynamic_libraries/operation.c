#include <stdio.h>

/**
 * add - adds two integers
 * @a:first int
 * @b : second int
 * Return:result
 */
int add(int a, int b)
{
	int result = a + b;

	return (result);
}

/**
 * sub - subtracts two integers
 * @a:first int
 * @b : second int
 * Return:result
 */
int sub(int a, int b)
{
	int result = a - b;

	return (result);
}

/**
 * div - divides two integers
 * @a:first int
 * @b : second int
 * Return:result
 */
int div(int a, int b)
{
	int result = a / b;

	return (result);
}

/**
 * mod - return remainderof two integers
 * @a:first int
 * @b : second int
 * Return:result
 */
int mod(int a, int b)
{
	int result = a % b;

	return (result);
}

/**
 * mul - multiplies two integers
 * @a:first int
 * @b : second int
 * Return:result
 */
int mul(int a, int b)
{
	int result = a * b;

	return (result);
}
